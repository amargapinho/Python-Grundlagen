meineNote = int(input("Note:"))

if(meineNote == 1):
    print("Sehr gut!")

elif(meineNote == 2):
    print("Gut!")

elif(meineNote == 3):
    print("Befriedigend")

elif(meineNote == 4):
    print("Ausreichend")

elif(meineNote == 5):
    print("Mangelhaft")

elif(meineNote == 6):
    print("Ungenuegend")

else:
    print("Deine Note soll zwischen 1 und 6 sein")

